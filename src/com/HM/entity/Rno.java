package com.HM.entity;

public class Rno {
	
	private String rno;
	private String  rent;
	
	@Override
	public String toString() {
		return "Rno [rno=" + rno + ", rent=" + rent + "]";
	}

	public String getRno() {
		return rno;
	}

	public void setRno(String rno) {
		this.rno = rno;
	}

	public String getRent() {
		return rent;
	}

	public void setRent(String rent) {
		this.rent = rent;
	}
	
	

	
	
	
	

}
